#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;
	long	nb;

	nb = n;
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb / 10)
		ft_putnbr(nb / 10);
	c = '0' + nb % 10;
	ft_putchar(c);
}
